/**
 * Detect locale from browser language string.
 * Falls back to 'en' for unsupported locales.
 */
export function getLocale(lang?: string): Locale {
  if (!lang) return 'en'
  const code = lang.slice(0, 2).toLowerCase()
  if (SUPPORTED_LOCALES.includes(code as Locale)) return code as Locale
  return 'en'
}

/**
 * Translate helper. Returns translated string or falls back to en, then to key.
 */
export function t(key: string, locale: Locale): string {
  return translations[locale]?.[key] || translations['en']?.[key] || key
}
